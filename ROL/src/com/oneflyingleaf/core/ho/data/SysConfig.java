package com.oneflyingleaf.core.ho.data;

public class SysConfig {
	private Integer sysConfigId;
	private String sysConfigKey;
	private String sysConfigVal;
	private String sysConfigGrp;
	private String sysConfigCom;
	private String sysConfigCode;
	private String sysConfigStat;
	
	
	public Integer getSysConfigId() {
		return sysConfigId;
	}
	public void setSysConfigId(Integer sysConfigId) {
		this.sysConfigId = sysConfigId;
	}
	public String getSysConfigKey() {
		return sysConfigKey;
	}
	public void setSysConfigKey(String sysConfigKey) {
		this.sysConfigKey = sysConfigKey;
	}
	public String getSysConfigVal() {
		return sysConfigVal;
	}
	public void setSysConfigVal(String sysConfigVal) {
		this.sysConfigVal = sysConfigVal;
	}
	public String getSysConfigGrp() {
		return sysConfigGrp;
	}
	public void setSysConfigGrp(String sysConfigGrp) {
		this.sysConfigGrp = sysConfigGrp;
	}
	public String getSysConfigCom() {
		return sysConfigCom;
	}
	public void setSysConfigCom(String sysConfigCom) {
		this.sysConfigCom = sysConfigCom;
	}
	public String getSysConfigCode() {
		return sysConfigCode;
	}
	public void setSysConfigCode(String sysConfigCode) {
		this.sysConfigCode = sysConfigCode;
	}
	public String getSysConfigStat() {
		return sysConfigStat;
	}
	public void setSysConfigStat(String sysConfigStat) {
		this.sysConfigStat = sysConfigStat;
	}
}
